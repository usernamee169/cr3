'''
HomeDyn14. К-ичные числа. Среди чисел в системе счисления с основанием K (2≤K≤10)
определить сколько имеется чисел из N (1<N<20, N+K<26) разрядов таких, что в их
записи содержится два и более подряд идущих нулей.

Языковая модель: https://trychatgpt.ru
'''

def count_numbers_with_consecutive_zeros(K, N):
    # Генерируем все возможные числа в системе счисления с основанием K
    count = 0
    max_number = K ** N  # Максимальное число в системе счисления K с N разрядами

    for i in range(max_number):
        # Преобразуем число i в систему счисления K
        number_in_base_k = []
        num = i
        
        while num > 0:
            number_in_base_k.append(num % K)
            num //= K
        
        # Заполняем нулями до длины N
        while len(number_in_base_k) < N:
            number_in_base_k.append(0)
        
        # Переворачиваем список, чтобы получить правильный порядок разрядов
        number_in_base_k.reverse()
        
        # Проверяем наличие двух и более подряд идущих нулей
        if '00' in ''.join(map(str, number_in_base_k)):
            count += 1

    return count

# Пример использования функции для различных значений K и N
K = 3  # Основание системы счисления (например, 3)
N = 5  # Количество разрядов (например, 5)

result = count_numbers_with_consecutive_zeros(K, N)
print(f"Количество чисел с {N} разрядами в системе счисления с основанием {K}, содержащих два и более подряд идущих нуля: {result}")